 # If
 In its simplest form, the if statement has a single if condition. It executes a set of statements only if that condition is true.
 
 ## Syntax
 ```
 if CONDITION {
    STATEMENTS
 } else if CONDITION {
    STATEMENTS
 } else {
    STATEMENTS
 }
 ```
 
