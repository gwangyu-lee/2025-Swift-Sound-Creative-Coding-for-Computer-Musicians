 # Enum (Enumeration)
 
 An `enum` defines a **common type for a group of related values** and enables you to work with those values in a type-safe way.
 
 ## Syntax
 ```
 enum NAME {
    case CASE_ONE
    case CASE_TWO
 }
 ```
 
 ```
 enum Waveforms {
     case sine
     case saw
     case rect
     case square
     case tri
 }

 var myWaveform = Waveforms.sine

 switch myWaveform {
 case .sine:
     print("Sine wave")
 case .saw:
     print("Sawtooth wave")
 case .rect:
     print("Rectangular wave")
 case .square:
     print("Square wave")
 case .tri:
     print("Triangle wave")
 }
 ```
